export default {
  data() {
    return {
      username: ''
    }
  },
  methods: {
    logout() {
      this.$cookies.remove('auth-token')
      this.$emit('logout', false)
    },
    checkusername() {
      var ca = this.$cookies.get("auth-token")
      var base64Url = ca.split('.')[1]
      var decodedValue = JSON.parse(window.atob(base64Url))
      this.username = decodedValue.sub
    },
    
  },
  mounted() {
    this.checkusername()
